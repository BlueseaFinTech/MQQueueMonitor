package org.erc.qmm.monitor;

/**
 * The Class PollEvent.
 */
public final class PollEvent {

	/** The enqueued. */
	private int enqueued;
	
	/** The dequeued. */
	private int dequeued;
	
	/** The depth. */
	private int depth;
	
	/** The max depth. */
	private int maxDepth;

	/**
	 * Gets the enqueued.
	 *
	 * @return the enqueued
	 */
	public int getEnqueued() {
		return enqueued;
	}

	/**
	 * Sets the enqueued.
	 *
	 * @param enqueued the new enqueued
	 */
	public void setEnqueued(int enqueued) {
		this.enqueued = enqueued;
	}

	/**
	 * Gets the dequeued.
	 *
	 * @return the dequeued
	 */
	public int getDequeued() {
		return dequeued;
	}

	/**
	 * Sets the dequeued.
	 *
	 * @param dequeued the new dequeued
	 */
	public void setDequeued(int dequeued) {
		this.dequeued = dequeued;
	}

	/**
	 * Gets the depth.
	 *
	 * @return the depth
	 */
	public int getDepth() {
		return depth;
	}

	/**
	 * Sets the depth.
	 *
	 * @param depth the new depth
	 */
	public void setDepth(int depth) {
		this.depth = depth;
	}

	/**
	 * Gets the max depth.
	 *
	 * @return the max depth
	 */
	public int getMaxDepth() {
		return maxDepth;
	}

	/**
	 * Sets the max depth.
	 *
	 * @param maxDepth the new max depth
	 */
	public void setMaxDepth(int maxDepth) {
		this.maxDepth = maxDepth;
	}
	
	
	
	
}
